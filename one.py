# Data Types in Python

# string
a = "uvesh"
print(a)

# int
b = 7
print(b)

# float
c = 9.99
print(c)

# list
d = ["a","b","c","d",]
print(d)

# tuple
e = ("a","b","c","d")
print(e)

# frozenset
h = ({"a","b","c","d"})
print(h)

# bool
f = True
print(f)

# nonetype
g = None
print(g)

# Bytes
i = b"Hello"
print(i)

# Range
j = range(5)
print(j)

# complex numbers
k = 3j
print(k)
